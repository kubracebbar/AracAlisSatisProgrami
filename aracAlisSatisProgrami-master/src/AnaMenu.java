    private ArrayList<String> executeQuery(String sql){
        Connection connection = null;
        Statement statement = null;
        ResultSet resultSet = null;
        ArrayList<String> results = new ArrayList<>();
        try{
            connection = MySQLConnection.getConnection();
            statement = connection.createStatement();
            resultSet = statement.executeQuery(sql);
            while(resultSet.next()){
                results.add(resultSet.getString(1));
            }
        }catch(SQLException ex){}
        finally{
            try{ if(resultSet != null) resultSet.close(); } catch(SQLException ex) {}
            try{ if(statement != null) statement.close(); } catch(SQLException ex) {}
            try{ if(connection != null) connection.close(); } catch(SQLException ex) {}
        }
        return results;
    }
    
    private ArrayList<Date> executeQueryGetDate(String sql){
        Connection connection = null;
        ArrayList<Date> results = new ArrayList<>();
        Statement statement = null;
        ResultSet resultSet = null;
        try{
            connection = MySQLConnection.getConnection();
            statement = connection.createStatement();
            resultSet = statement.executeQuery(sql);
            while(resultSet.next()){
                results.add(resultSet.getDate(1));
            }
        }catch(SQLException ex){}
        finally{
            try{ if(resultSet != null) resultSet.close(); } catch(SQLException ex) {}
            try{ if(statement != null) statement.close(); } catch(SQLException ex) {}
            try{ if(connection != null) connection.close(); } catch(SQLException ex) {}
        }
        return results;
    }
    
    private void executeUpdate(String sql){
        Connection connection = null;
        Statement statement = null;
        try{
            connection = MySQLConnection.getConnection();
            statement = connection.createStatement();
            statement.executeUpdate(sql);
        }catch(SQLException ex){}
        finally{
            try{ if(statement != null) statement.close(); } catch(SQLException ex) {}
            try{ if(connection != null) connection.close(); } catch(SQLException ex) {}
        }
    }
    
